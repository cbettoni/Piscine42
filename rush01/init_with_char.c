#define N 4

void	ft_init_with_char(char *str, int *array)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= '1' && str[i] <= '4')
		{
			array[j] = str[i] - '0';
			j++;
			i++;
		}
		i++;
	}
}
